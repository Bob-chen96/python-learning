import platform

# 获取操作系统名称和版本号
print(platform.platform())

# 获取操作系统类型
print(platform.system())

# 获取操作系统版本号
print(platform.version())

# 获取操作系统的位数
print(platform.architecture())

# 计算机类型
print(platform.machine())

# 计算机网络名称
print(platform.node())

# 计算机处理器信息
print(platform.processor())

# 包含上面所有的信息汇总
print(platform.uname())

